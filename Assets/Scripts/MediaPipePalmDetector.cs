        public MediaPipePalmDetector(NNModel model, float nmsThreshold = 0.3f, float scoreThreshold = 0.5f, int topK = 5000)
        {

            palm_detection_net = model;
            nms_threshold = Mathf.Clamp01(nmsThreshold);
            score_threshold = Mathf.Clamp01(scoreThreshold);
            this.topK = topK;

            anchors = load_anchors();
        }

        public void Initialize()
        {
            // Load the model from the provided NNModel asset
            var model = ModelLoader.Load(palm_detection_net);

            // Create a Barracuda worker to run the model on the GPU
            worker = WorkerFactory.CreateWorker(WorkerFactory.Type.ComputePrecompiled, model);
        }

        public Texture2D preprocess(Texture2D tex)
        {
            // Pad image to make square
            int maxSize = Mathf.Max(tex.width, tex.height);
            int offsetX = (maxSize - tex.width) / 2;
            int offsetY = (maxSize - tex.height) / 2;
            Debug.Log("Max: " + maxSize.ToString()
                + "\tOffsetX: " + offsetX.ToString()
                + "\tOffsety: " + offsetY.ToString());

            Texture2D paddedTex = new Texture2D(maxSize, maxSize, TextureFormat.ARGB32, false);
            Color[] texPixels = tex.GetPixels();
            paddedTex.SetPixels(offsetX, offsetY, tex.width, tex.height, texPixels);
            paddedTex.Apply();

            RenderTexture.active = null;

            return paddedTex;
        }

        public async Task<int> DetectPalms(Texture2D texture)
        {
            var inputTensor = new Tensor(texture, channels: 3);
            // Run the model on the input tensor
            var outputTensor = await ForwardAsync(worker, inputTensor);
            inputTensor.Dispose();

            var id = outputTensor.uniqueId;
            var shape = outputTensor.shape;
            Debug.Log($"tensor shape: [{string.Join(',', shape)}]");
            await Task.Delay(32);

            outputTensor.Dispose();
            return id;
        }

        // Nicked from https://github.com/Unity-Technologies/barracuda-release/issues/236#issue-1049168663
        public async Task<Tensor> ForwardAsync(IWorker modelWorker, Tensor inputs)
        {
            var executor = worker.StartManualSchedule(inputs);
            var it = 0;
            bool hasMoreWork;
            do
            {
                hasMoreWork = executor.MoveNext();
                if (++it % 20 == 0)
                {
                    worker.FlushSchedule();
                    await Task.Delay(32);
                }
            } while (hasMoreWork);

            return modelWorker.PeekOutput();
        }

