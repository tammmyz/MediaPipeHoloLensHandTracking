{
    ModelAsset palm_detection_net;
    float nms_threshold;
    float score_threshold;
    int topK;
    Mat anchors;

    Size input_size = new Size(192, 192);
    Mat boxesMat;
    Mat boxes_m_c4;
    Mat confidences_m;
    MatOfRect2d boxes;
